import math
# 数字类型
# 复数
a = complex(1, 2)
print(a)
# (1 + 2j)
print(int(1.59))
# 1

b = 1 / 3
print(b)
# 0.3333333
print('%4.2f' % b)
# 0.33
print('{0:4.2f}'.format(b))
# 0.33

x = 1
y = 2
z = 3
print(x < y < z)
# True

print(1 / 3)
# 0.333333
print(1 // 3)
# 0

# 向下取整 截断除法
print(math.floor(2.5))
# 2

print(9 / 3, 9 // 3)
# (3.0 3)
